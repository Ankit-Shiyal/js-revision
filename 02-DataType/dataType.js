

//premitive data type


//1.string

let string = "Ankit";

console.log(string);
console.log(typeof string);


//2.bigint

let BigInt = 7573068252n;

console.log(BigInt);
console.log(typeof BigInt);


//3.number

let number = 11281;

console.log(number);
console.log(typeof number);


//4.undefine

let a;
console.log(typeof a);


//5.Boolean

let A = true;

console.log(A);
console.log(typeof A);


//6.symbol

let b = Symbol(10);
let c = Symbol(10);

console.log(b == c);



//7.null

let NULL = null;

console.log(typeof NULL);





//non-premitive data types

//8.object


const Student = { NAME: "Ankit", Age: "20" };

console.log(Student);
console.log(typeof Student);

Student.NAME="Shiyal"

console.log(Student)


